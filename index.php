<h2 class="section-heading wow bounceIn"><i class="glyphicon glyphicon-info-sign" ></i> Know About Us </h2>
<hr class=light>
<p style=text-align:justify >The Indian Institutes of Technology are the most regarded institutions for science and technology in the nation and continue to stand for the continual development and progress of India. Over the years, it became increasingly evident that keeping the highest standards alive and furthering the momentum of progress required a different, competitive platform to engage the best among the best, in an effort to propel greater heights and motivation for the talented youth. It became important to have a platform where the best brains of the country can compete and also share their thoughts which can help in the progress of our nation.</p>
<p style=text-align:justify>In this regard, Indian Institute of Technology Kanpur proposed and laid out the framework for the 'common platform' which then became the Inter IIT Tech Meet - the biggest arena for testing each individual's and Institute's brightest minds in a war of intelligence, excellence and innovation. With the idea accepted, the 1st Inter IIT Tech Meet was hosted by IIT Kanpur in 2013, and was a great success, paving the way for the recurring annual event to be rotated amongst the different IITs from year to year with the second and third editions being held in IIT Bombay and IIT Kharagpur respectively. In keeping with this tradition, IIT Mandi promises to continue with the 4<sup>th</sup> edition of the Inter IIT Tech Meet in 2016 from January 29 to January 31.</p>
